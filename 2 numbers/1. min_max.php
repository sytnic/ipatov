<?php
// 1. Определение максимального и минимального числа

// Пример 1, сравнить два числа
$a = 1;
$b = 3;
$min = min($a, $b);
$max = max($a, $b);
echo "min: $min<br/>";  // 1
echo "max: $max<br/>";  // 3
// Пример 2, сравнить 7 чисел
$min = min(1, 2, -5, 6, 0, -8, 7);
$max = max(1, 2, -5, 6, 0, -8, 7);
echo "min: $min<br/>";  // -8
echo "max: $max";       // 7

?>